// 6. 数字汉字化（4条规则）
// ════════════════════════════════════════════

const DIGIT_TO_CHINESE: Record<string, string> = {
  '0': '零', '1': '一', '2': '二', '3': '三', '4': '四',
  '5': '五', '6': '六', '7': '七', '8': '八', '9': '九'
}

const DECADE_MAP: Record<string, string> = {
  '10': '十', '20': '二十', '30': '三十', '40': '四十', '50': '五十',
  '60': '六十', '70': '七十', '80': '八十', '90': '九十'
}

function smallNumberToChinese(n: number): string {
  if (n <= 0 || n > 99) return String(n)
  if (n <= 9) return DIGIT_TO_CHINESE[String(n)]
  if (n === 10) return '十'
  if (n < 20) return '十' + DIGIT_TO_CHINESE[String(n % 10)]
  const tens = DIGIT_TO_CHINESE[String(Math.floor(n / 10))] + '十'
  return n % 10 === 0 ? tens : tens + DIGIT_TO_CHINESE[String(n % 10)]
}

function applyDigitNormalization(text: string): string {
  // 4位年份 1900-2099 → 逐位汉字
  let result = text.replace(
    /(?<!\d)((?:19|20)\d{2})(?=年)/g,
    (_, year: string) => year.split('').map((d) => DIGIT_TO_CHINESE[d]).join('')
  )
  // 年代 80年代 → 八十年代
  result = result.replace(/(?<!\d)(\d0)年代/g, (_, d: string) => (DECADE_MAP[d] ?? d) + '年代')
  // 月份 1-12 → 汉字
  result = result.replace(
    /(?<![\d年个])([1-9]|1[0-2])(?=月(?:\D|$))/g,
    (_, m: string) => smallNumberToChinese(parseInt(m))
  )
  // 日/号 1-31 → 汉字
  result = result.replace(
    /(?<![\d月])([1-9]|[12]\d|3[01])(?=[日号])/g,
    (_, d: string) => smallNumberToChinese(parseInt(d))
  )
  return result
}

