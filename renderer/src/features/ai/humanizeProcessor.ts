/**
 * 对 AI 生成的小说正文应用完整去痕管道（pre_llm 阶段）。
 *
 * 管道顺序：
 *   AI元语言删段 → Markdown清理 → AI模板删段 → 废话词删除
 *   → 数字汉字化 → 口语短语替换(50%) → 套话短语替换(85%)
 *   → 口语词替换(50%) → 通用词替换(50%) → 标点规范化 → 空行收缩
 */
export function humanizeText(text: string): string {
  let r = text
  r = removeAiArtifactParagraphs(r)
  r = cleanMarkdownArtifacts(r)
  r = removeAiTemplateParagraphs(r)
  r = removeAiFillerPhrases(r)
  r = applyDigitNormalization(r)
  r = applyDictionary(r, NOVEL_ORAL_PHRASES, 0.50)
  r = applyDictionary(r, PHRASE_REPLACEMENTS, 0.85)
  r = applyDictionary(r, NOVEL_ORAL_WORDS, 0.50)
  r = applyDictionary(r, WORD_REPLACEMENTS, 0.50)
  r = normalizeEllipsis(r)
  r = normalizeDash(r)
  r = collapseBlankLines(r)
  return r.trim()
}

/**
 * post_llm 阶段（"降低AI感"快捷动作结果的二次清理）。
 */
export function humanizeTextPost(text: string): string {
  let r = text
  r = applyDigitNormalization(r)
  r = removeAiFillerPhrases(r)
  r = normalizeEllipsis(r)
  r = normalizeDash(r)
  return r.trim()
}
