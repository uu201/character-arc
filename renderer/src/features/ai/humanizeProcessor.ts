const AI_ARTIFACT_PATTERNS = [
  /我将按照您的/,
  /按照您的要求/,
  /根据您(?:供给|提供)的/,
  /以下是我(?:根据|为您|按照)/,
  /故事梗概/,
  /本次写作(?:重点|将|主要)/,
  /接下来故事(?:可能|将|会)/,
  /这是一个关于.{0,20}的故事/
] as const

function removeAiArtifactParagraphs(text: string): string {
  return text
    .split('\n\n')
    .filter((para) => !AI_ARTIFACT_PATTERNS.some((p) => p.test(para)))
    .join('\n\n')
}

// ════════════════════════════════════════════
// 3. Markdown 残留清理（3条）
// ════════════════════════════════════════════

function cleanMarkdownArtifacts(text: string): string {
  return text
    .replace(/^\s*#{1,4}\s+.*$/gm, '')
    .replace(/^\s*[-*]\s+\*\*[^*]+\*\*[:：].*$/gm, '')
    .replace(/^\s*(?:---+|\*\*\*+|===+)\s*$/gm, '')
}

