// ════════════════════════════════════════════
// 辅助：带概率的随机候选替换
// ════════════════════════════════════════════

function pickCandidate(candidates: readonly string[]): string {
  return candidates[Math.floor(Math.random() * candidates.length)]
}

function applyDictionary(
  text: string,
  dict: Record<string, readonly string[]>,
  rate: number
): string {
  // 按 key 长度降序，避免短 key 先吃掉长 key 的前缀
  const keys = Object.keys(dict).sort((a, b) => b.length - a.length)
  let result = text
  for (const key of keys) {
    if (!result.includes(key)) continue
    const candidates = dict[key]
    result = result.split(key).map((seg, idx, arr) => {
      if (idx === arr.length - 1) return seg
      return Math.random() < rate ? seg + pickCandidate(candidates) : seg + key
    }).join('')
  }
  return result
}

